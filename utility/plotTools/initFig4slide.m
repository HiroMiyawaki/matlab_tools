function fh=initFig4slide()

fh=figure('position',[-1000,1000,1000,600]);

set(fh,'defaultAxesFontSize',20);
set(fh,'defaultTextFontSize',20);
set(fh,'defaultAxesLineWidth', 1.5);
set(fh,'defaultLineLineWidth', 2); 

set(fh,'DefaultAxesXGrid','off');
set(fh,'DefaultAxesYGrid','off');
set(fh,'DefaultAxesBox','off');
set(fh,'PaperPositionMode','auto');

end